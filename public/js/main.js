//email funcction
function sendEmail(){
	var name = $("#name").val();
	var email = $("#email").val();
	var message = $("#message").val();
    var action = "http://analyticmachine.com/sendemail";
    var data = {
    	name: name,
    	email: email,
    	message: message
    };
    $('.form-field').css('background', '#ffffff');
    $('#success_email_msg').html('');

    $.ajax({
    	type: "post",
    	url: action,
    	data: data,
    	cache: false,
    	success: function(html){
    		var data = jQuery.parseJSON(html);
    		if(data.msg == "success"){
    			$('#success_email_msg').html('Thanks for contacting us!');
    		}else{
    			$.each(data.errors, function(i, v){
    				$('#'+i).css('background', '#febfbb');
    			});
    		}
    	}
    });

  return false;
}

